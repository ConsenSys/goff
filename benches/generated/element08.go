// Sqrt z = √x mod q
// if the square root doesn't exist (x is not a square mod q)
// Sqrt leaves z unchanged and returns nil
func (z *Element08) Sqrt(x *Element08) *Element08 {
	// q ≡ 5 (mod 8)
	// see modSqrt5Mod8Prime in math/big/int.go
	var one, alpha, beta, tx, square Element08
	one.SetOne()
	tx.Double(x)
	alpha.Exp(tx,
		9156475537852485935,
		11911751703907578972,
		14848439410435450271,
		17652883022453527331,
		5679131837850871993,
		1123521512854437823,
		5552313014212115039,
		1015454701602332244,
	)
	beta.Square(&alpha).
		MulAssign(&tx).
		SubAssign(&one).
		MulAssign(x).
		MulAssign(&alpha)

	square.Square(&beta)
	if square.Equal(x) {
		return z.Set(&beta)
	} else {
		return nil
	}
}

// Square z = x * x mod q
func (z *Element08) Square(x *Element08) *Element08 {
	return z.Mul(x, x)
}
