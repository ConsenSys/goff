// Legendre returns the Legendre symbol of z (either +1, -1, or 0.)
func (z *Element05) Legendre() int {
	var l Element05
	// z^((q-1)/2)
	l.Exp(*z,
		17266386592089710720,
		1334753172746901086,
		8990650224792966001,
		10068609140368705294,
		3969987452675380758,
	)

	if l.IsZero() {
		return 0
	}

	// if l == 1
	if (l[4] == 2566794263008028581) && (l[3] == 15065795659653833670) && (l[2] == 930887248247239227) && (l[1] == 13107731382721947268) && (l[0] == 4721429926479363582) {
		return 1
	}
	return -1
}

// Sqrt z = √x mod q
// if the square root doesn't exist (x is not a square mod q)
// Sqrt leaves z unchanged and returns nil
func (z *Element05) Sqrt(x *Element05) *Element05 {
	// q ≡ 1 (mod 4)
	// see modSqrtTonelliShanks in math/big/int.go
	// using https://www.maa.org/sites/default/files/pdf/upload_library/22/Polya/07468342.di020786.02p0470a.pdf

	var y, b, t, w Element05
	// w = x^((s-1)/2))
	w.Exp(*x,
		6840860662190576416,
		8147722005866899350,
		1043926043971588627,
		1624597573288979847,
		15507763487013206,
	)

	// y = x^((s+1)/2)) = w * x
	y.Mul(x, &w)

	// b = x^s = w * w * x = y * x
	b.Mul(&w, &y)

	// g = nonResidue ^ s
	var g = Element05{
		2202373857752672334,
		9213397571971690399,
		8571019582413512913,
		8426407916196166254,
		3545833044640105182,
	}
	r := uint64(8)

	// compute legendre symbol
	// t = x^((q-1)/2) = r-1 squaring of x^s
	t = b
	for i := uint64(0); i < r-1; i++ {
		t.Square(&t)
	}
	if t.IsZero() {
		return z.SetZero()
	}
	if !((t[4] == 2566794263008028581) && (t[3] == 15065795659653833670) && (t[2] == 930887248247239227) && (t[1] == 13107731382721947268) && (t[0] == 4721429926479363582)) {
		// t != 1, we don't have a square root
		return nil
	}
	for {
		var m uint64
		t = b

		// for t != 1
		for !((t[4] == 2566794263008028581) && (t[3] == 15065795659653833670) && (t[2] == 930887248247239227) && (t[1] == 13107731382721947268) && (t[0] == 4721429926479363582)) {
			t.Square(&t)
			m++
		}

		if m == 0 {
			return z.Set(&y)
		}
		// t = g^(2^(r-m-1)) mod q
		ge := int(r - m - 1)
		t = g
		for ge > 0 {
			t.Square(&t)
			ge--
		}

		g.Square(&t)
		y.MulAssign(&t)
		b.MulAssign(&g)
		r = m
	}
}

// Square z = x * x mod q
func (z *Element05) Square(x *Element05) *Element05 {
	return z.Mul(x, x)
}
