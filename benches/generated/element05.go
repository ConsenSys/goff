		m := z[0] * 10513151696181630721
		C := madd0(m, 4037226758542628607, z[0])
		C, z[0] = madd2(m, 10260335205665271989, z[1], C)
		C, z[1] = madd2(m, 14864618863198748756, z[2], C)
		C, z[2] = madd2(m, 15971128581955862924, z[3], C)
		C, z[3] = madd2(m, 6919214387822879545, z[4], C)
