func (z *Element05) Legendre() int {
	var l Element05
	// z^((q-1)/2)
	l.Exp(*z,
		11241985416126090111,
		5130167602832635994,
		7432309431599374378,
		17208936327832707270,
		3459607193911439772,
	)

	if l.IsZero() {
		return 0
	}

	// if l == 1
	if (l[4] == 4608315298063792524) && (l[3] == 4951230983507377382) && (l[2] == 7164250421021605718) && (l[1] == 16372817736088559253) && (l[0] == 10372290556624294402) {
		return 1
	}
	return -1
}

// Sqrt z = √x mod q
// if the square root doesn't exist (x is not a square mod q)
// Sqrt leaves z unchanged and returns nil
func (z *Element05) Sqrt(x *Element05) *Element05 {
	// q ≡ 3 (mod 4)
	// using  z ≡ ± x^((p+1)/4) (mod q)
	var y, square Element05
	y.Exp(*x,
		5620992708063045056,
		2565083801416317997,
		3716154715799687189,
		8604468163916353635,
		1729803596955719886,
	)
	square.Square(&y)
	if square.Equal(x) {
		return z.Set(&y)
	} else {
		return nil
	}
}

// Square z = x * x mod q
func (z *Element05) Square(x *Element05) *Element05 {
	return z.Mul(x, x)
}
