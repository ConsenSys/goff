// Sqrt z = √x mod q
// if the square root doesn't exist (x is not a square mod q)
// Sqrt leaves z unchanged and returns nil
func (z *Element09) Sqrt(x *Element09) *Element09 {
	// q ≡ 5 (mod 8)
	// see modSqrt5Mod8Prime in math/big/int.go
	var one, alpha, beta, tx, square Element09
	one.SetOne()
	tx.Double(x)
	alpha.Exp(tx,
		14339943972592636841,
		5299573980332552654,
		11568312751742779571,
		2569802757866473095,
		7863015975981009156,
		10566481624180862513,
		365219476111474670,
		11913729914582909531,
		1023221365330961161,
	)
	beta.Square(&alpha).
		MulAssign(&tx).
		SubAssign(&one).
		MulAssign(x).
		MulAssign(&alpha)

	square.Square(&beta)
	if square.Equal(x) {
		return z.Set(&beta)
	} else {
		return nil
	}
}

